// Function to add two numbers
function addNumbers(num1, num2) {
    // Add num1 and num2
    let sum = num1 + num2;
    
    // Return the result
    return sum;
}


// Test cases
console.log(addNumbers(3, 5)); // Expected output: 8
console.log(addNumbers(-1, 7)); // Expected output: 6
console.log(addNumbers(0, 0)); // Expected output: 0


